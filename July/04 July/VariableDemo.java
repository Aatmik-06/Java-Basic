// WAP to display name age section of a student 

class VariableDemo{
    public static void main(String[] args){
    //    Declare variable 
    double marks;
    int age;
    char section;
    // initilization
    // identifier = value or literal
    marks = 50.1;
    age = 40;  
    section = 'D';
    // printing variable values or output
    System.out.println("Marks=" + marks);  // it will print and also comes in newline // so "Marks=" and marks will also get converted into string
    System.out.println("Age=" + age); // so "Age=" and age will also get converted into string
    System.out.println("Section=" + section); // so "Section=" and section will also get converted into string
    }
}

